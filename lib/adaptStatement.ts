import type { AssessorRole } from "@/types";

const VERB_REPLACEMENTS: [RegExp, string][] = [
  [/^Я Я беру\b/, "Исследователь берёт"],
  [/^Я умеею\b/, "Исследователь умеет"],
  [/^Я умею\b/, "Исследователь умеет"],
  [/^Я понимаю\b/, "Исследователь понимает"],
  [/^Я формулирую\b/, "Исследователь формулирует"],
  [/^Я различаю\b/, "Исследователь различает"],
  [/^Я настраиваю\b/, "Исследователь настраивает"],
  [/^Я избегаю\b/, "Исследователь избегает"],
  [/^Я проверяю\b/, "Исследователь проверяет"],
  [/^Я оцениваю\b/, "Исследователь оценивает"],
  [/^Я проектирую\b/, "Исследователь проектирует"],
  [/^Я учитываю\b/, "Исследователь учитывает"],
  [/^Я закладываю\b/, "Исследователь закладывает"],
  [/^Я принимаю\b/, "Исследователь принимает"],
  [/^Я могу\b/, "Исследователь может"],
  [/^Я замечаю\b/, "Исследователь замечает"],
  [/^Я строю\b/, "Исследователь строит"],
  [/^Я применяю\b/, "Исследователь применяет"],
  [/^Я интерпретирую\b/, "Исследователь интерпретирует"],
  [/^Я сегментирую\b/, "Исследователь сегментирует"],
  [/^Я превращаю\b/, "Исследователь превращает"],
  [/^Я влияю\b/, "Исследователь влияет"],
  [/^Я беру\b/, "Исследователь берёт"],
  [/^Я корректно\b/, "Исследователь корректно"],
  [/^Я обоснованно\b/, "Исследователь обоснованно"],
];

export function adaptStatement(
  statement: string,
  role: AssessorRole
): string {
  if (role === "self") return statement;

  for (const [pattern, replacement] of VERB_REPLACEMENTS) {
    if (pattern.test(statement)) {
      return statement.replace(pattern, replacement);
    }
  }

  return statement.replace(/^Я\b/, "Исследователь");
}
